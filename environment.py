import numpy as np

class Env:
    def __init__(self):
        self.map = np.zeros([2, 25, 25])
        # print("init success")

    def move(self, player=0, pos_x=0, pos_y=0):
        # 添加padding
        pos_x += 5
        pos_y += 5
        self.map[player, pos_x, pos_y] = 1          # 选手落子
        state = self.check(player, pos_x, pos_y)    # 判断是否赢下对局
        return state

    def observe(self):
        user_map = self.map[:, 5:-5, 5:-5]

        not_pos = np.argwhere(user_map == 1)
        is_pos = self.where_is()
        # print(is_pos)
        return user_map, not_pos[:, 1:], not_pos[:, 0], is_pos

    def where_is(self):
        user_map = self.map[:, 5:-5, 5:-5]
        is_list = []
        for i in range(15):
            for j in range(15):
                if user_map[0, i, j] == 0 and user_map[1, i, j] == 0:
                    is_list.append([i, j])
        return np.array(is_list)

